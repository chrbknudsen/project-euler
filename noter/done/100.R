OK. Det er de der kuber, og den korteste rute fra et punkt til et andet, defineret som de
fjerneste hjørner, via rette linier på fladerne. 

Det første liniestykke kan vi vælge på tre måder.
Det andet liniestykke er givet når vi har valgt det første.
Lad os kalde de tre måder gulv, gavl og væg.
kuben har dimensionerne 6, 5, 3
Gulvet - og væggen - har derfor længden 6.
Gavlen har længden 5
Højden er 3

Går vi ad gulvet, skal vi vælge et punkt mellem 0 og 6. Lad os kalde det c
Længden af liniestykket l er så givet ved ved l² = c^2 + 5²
Derfra vil vi skulle gået et nyt liniestykke, med længden m. Det er defineret ved:
  m² = 3^2 + (6-c)²

Hvis vi i stedet går via gavlen, vælger vi også et punkt. Lad os kalde det d. Det kan 
vælges frit mellem 0 og 3
Længden n er defineret ved: n² = 5^2 + d^2
Det resterende liniestykke er så givet ved: p^2 = 6² + (3-d)^2